    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        try {
            btnBotones.add(Estudiante);
            btnBotones.add(Profesor);

            btnGeneros.add(Masculino);
            btnGeneros.add(Femenino);

            btnEstados.add(Activo);
            btnEstados.add(Inactivo);

            Estudiante nuevoEstudiante = new  Estudiante();
            Profesor   nuevoProfesor = new Profesor();
            String nombre = txtNombrePersona.getText();
            String apellido = txtApellidoPersona.getText();
            int dpi = Integer.valueOf(txtDpiPersona.getText());
            String fechaNacimiento = txtFechaNacimientoPersona.getText();
            int telefono = Integer.valueOf(txtDpiPersona.getText());
            String dirreccion = txtDirreccionPersona.getText();
            String correo = txtCorreoPersona.getText();
            int edad = Integer.valueOf(txtEdadPersona.getText());
            String estado = null;
            String genero = null;

            if(Activo.isSelected()){
                estado = "Activo";
            }else if(Inactivo.isSelected()){
                estado = "Inactivo";
            }

            if(Masculino.isSelected()){
                genero = "Masculino";
            }else if(Femenino.isSelected()){
                genero = "Femenino";
            }
            DateFormat format = new SimpleDateFormat("DD/MM/YYYY");
            Date fecha = format.parse(fechaNacimiento);

            if(Estudiante.isSelected()){
                nuevoEstudiante.setNombre(nombre);
                nuevoEstudiante.setApellido(apellido);
                nuevoEstudiante.setDpi(dpi);
                nuevoEstudiante.setDireccion(dirreccion);
                nuevoEstudiante.setCorreo(correo);
                nuevoEstudiante.setEstado(estado);
                nuevoEstudiante.setGenero(genero);
                nuevoEstudiante.setTelefono(telefono);
                nuevoEstudiante.setEdad(edad);
                nuevoEstudiante.setFechaNacimiento(fecha);
                String mensaje = estudianteServiceImpl.guardarEstudiante(nuevoEstudiante);
                JOptionPane.showMessageDialog(null, mensaje);
                limpiar();
            }else if(Profesor.isSelected()){

                nuevoProfesor.setNombre(nombre);
                nuevoProfesor.setApellido(apellido);
                nuevoProfesor.setDpi(dpi);
                nuevoProfesor.setDireccion(dirreccion);
                nuevoProfesor.setCorreo(correo);
                nuevoProfesor.setEstado(estado);
                nuevoProfesor.setGenero(genero);
                nuevoProfesor.setTelefono(telefono);
                nuevoProfesor.setFechaNacimiento(fecha);
                nuevoProfesor.setEdad(edad);
                String mensaje = profesorServiceImpl.guardarProfesor(nuevoProfesor);
                JOptionPane.showMessageDialog(null, mensaje);
                limpiar();
            }
        } catch (ParseException ex) {
            Logger.getLogger(MenuPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnGuardarMouseClicked

